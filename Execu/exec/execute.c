#include "../../Header/minishell.h"

void	execute(t_cmdl *cmd, char **env)
{
	int	size;

	if (!cmd || g_shell.here_sigint)
		return ;
	size = cmdline_size(cmd);
	if (size == 1)
		run_sole_cmd(cmd, env, command_validity(cmd));
	else
		pipex(cmd, env);
}
